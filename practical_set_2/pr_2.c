#include<stdio.h>

int main(){
    int a,b;
    
    printf("Enter the value of a : \n");
    scanf("%d", &a);
  
    printf("Enter the value of b : \n");
    scanf("%d", &b);
  
    printf("The value of a+b is = %d\n", a+b);
    printf("The value of a-b is = %d\n", a-b);
    printf("The value of a*b is = %d\n", a*b);
    printf("The value of a/b is = %d\n", a/b);
    printf("The value of a%b is = %d\n", a%b);

 return 0;
}